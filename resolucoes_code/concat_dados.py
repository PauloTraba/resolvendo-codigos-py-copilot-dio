# Receber dois dados diferentes do usuário e concatena-los em uma única string.

# Solicita ao usuário que insira o primeiro dado
dado1 = input("Digite o primeiro dado: ")

# Solicita ao usuário que insira o segundo dado
dado2 = input("Digite o segundo dado: ")

# Concatena os dois dados em uma única string
resultado = dado1 + dado2

# Exibe o resultado da concatenação
print("Resultado da concatenação:", resultado)